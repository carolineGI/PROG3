<!--Descrição do automovel-->
O automóvel é um meio de transporte, muito utilizado para o deslocamento de pessoas e cargas, entre as mais diferentes distâncias. Ele é composto por diversas peças e diversos mecanismos, como o motor, a embreagem e o sistema elétrico. Desde que foi inventado, o automóvel precisava de um condutor, para poder funcionar, atualmente, com as novas tecnologias, um automóvel sem um motorista já está chegando no mercado e logo será muito comum. <br>

Objetos: <br>

Condutor -> é a pessoa que dirige o automóvel, é quem aciona todos os comandos; <br>
Mecanismos -> é um conunto de elementos, que unidos transmitem e/ou transformam movimentos e forças; <br>
Embreagem -> é um mecanismo do automóvel, responsável pela troca de marchas; <br>
Motor -> é o dispositivo que vai converter a energia liberada pela queima do combustivel, em energia mecânica, responsável pelo movimento do automóvel; <br>
Sistema Elétrico -> ele possui diversas funções, uma delas é possibilitar a partida do motor a combustão, é ele que produz a faísca, que vai gerar a combustão do combustível. <br>

